const express = require('express');
const auth = require('../../middlewares/auth');
const validate = require('../../middlewares/validate');
const noteValidation = require('../../validations/note.validation');
const noteController = require('../../controllers/note.controller');

const router = express.Router();

router
  .route('/')
  .post(auth('manageNotes'), validate(noteValidation.createNote), noteController.createNote)
  .get(auth('getNotes'), validate(noteValidation.getNotes), noteController.getNotes);

router
  .route('/:noteId')
  .get(auth('getNotes'), validate(noteValidation.getNote), noteController.getNote)
  .patch(auth('manageNotes'), validate(noteValidation.updateNote), noteController.updateNote)
  .delete(auth('manageNotes'), validate(noteValidation.deleteNote), noteController.deleteNote);

module.exports = router;

